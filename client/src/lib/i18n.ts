export const useI18n = create<I18nStore>((set, get) => ({
  lang: (typeof window !== "undefined" ? (localStorage.getItem("kal-el-lang") as Lang) : null) || "fr",
  setLang: (lang: Lang) => {
    localStorage.setItem("kal-el-lang", lang);
    set({ lang });
  },
  t: (key: string) => {
    const { lang } = get();
    return translations[lang]?.[key] || translations.en[key] || key;
  },
  dir: () => {
    const { lang } = get();
    return lang === "he" ? "rtl" : "ltr";
  },
}));
